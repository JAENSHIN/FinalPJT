export function classifyKsic(code) {
    for (const classification in ksicCodeMapping) {
        if (ksicCodeMapping[classification].includes(code)) {
            if (classification.includes('기업')) {
                return '기업';
            } else {
                return '일반 업종';
            }
        }
    }
    return '기타'; // 분류되지 않는 경우 '기타' 반환
}

export function groupBusinessesByKsicCd(businesses) {
    return businesses.reduce((acc, business) => {
        const ksicCd = business.ksicCd || '기타'; // 업종 코드가 없으면 '기타'로 설정
        if (!acc[ksicCd]) {
            acc[ksicCd] = { count: 0, name: business.ksicNm };
        }
        acc[ksicCd].count++;
        return acc;
    }, {});
}
